// Example usage of Voclio Logo widgets

// 1. Logo with Voclio text (used in AuthTitleInfo)
/*
VoclioLogo(
  size: 80.w,
  showText: true,
)
*/

// 2. Logo only without text
/*
VoclioLogo(
  size: 60.w,
  showText: false,
)
*/

// 3. Small logo for headers or navigation
/*
VoclioLogoSmall(
  size: 32.w,
  color: Colors.blue,
)
*/

// 4. Custom colored logo with text
/*
VoclioLogo(
  logoColor: Colors.purple,
  textColor: Colors.purple,
  showText: true,
)
*/
